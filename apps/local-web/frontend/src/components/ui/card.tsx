import type { HTMLAttributes } from "react";
import { cn } from "../../lib/utils";

interface CardProps extends HTMLAttributes<HTMLDivElement> {
  interactive?: boolean;
  panel?: boolean;
}

export function Card({ className, interactive = false, panel = false, ...props }: CardProps) {
  return (
    <div
      className={cn(
        "rounded-[var(--radius-lg)] bg-[var(--bg-card)] shadow-[var(--shadow-sm)]",
        panel ? "p-6" : "p-4",
        interactive &&
          "cursor-pointer hover:-translate-y-0.5 hover:shadow-[var(--shadow-md)] active:scale-[0.98]",
        className,
      )}
      {...props}
    />
  );
}
